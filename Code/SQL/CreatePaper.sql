CREATE TABLE Paper (
    ID COUNTER PRIMARY KEY,
    DOI VARCHAR,
    Title Memo,
    Authors Memo,
    [Address] Memo,
    ISSN VARCHAR,
    eISSN VARCHAR,
    ISBN VARCHAR,
    Year int,
    [Weight] float,
    SCIE float,
    SSCI float,
    ESCI float,
    [AHCI] float,
    [BKCI-S] float,
    [BKCI-SSH] float
);